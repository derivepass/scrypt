#include <stdio.h>
#include <string.h>

#include "test/test.h"
#include "src/common.h"
#include "src/sha256.h"

typedef struct scrypt_sha256_test_s scrypt_sha256_test_t;

struct scrypt_sha256_test_s {
  const char* inputs[16];
  uint8_t expected[32];
};

SCRYPT_TEST(sha256) {
  size_t i;

  scrypt_sha256_test_t tests[] = {
    {
      .inputs = { "abc", NULL },
      .expected = {
        0xBA, 0x78, 0x16, 0xBF,
        0x8F, 0x01, 0xCF, 0xEA,
        0x41, 0x41, 0x40, 0xDE,
        0x5D, 0xAE, 0x22, 0x23,
        0xB0, 0x03, 0x61, 0xA3,
        0x96, 0x17, 0x7A, 0x9C,
        0xB4, 0x10, 0xFF, 0x61,
        0xF2, 0x00, 0x15, 0xAD
      }
    },
    {
      .inputs = { "a", "b", "c", NULL },
      .expected = {
        0xBA, 0x78, 0x16, 0xBF,
        0x8F, 0x01, 0xCF, 0xEA,
        0x41, 0x41, 0x40, 0xDE,
        0x5D, 0xAE, 0x22, 0x23,
        0xB0, 0x03, 0x61, 0xA3,
        0x96, 0x17, 0x7A, 0x9C,
        0xB4, 0x10, 0xFF, 0x61,
        0xF2, 0x00, 0x15, 0xAD
      }
    },
    {
      .inputs = {
        "abcdbcdecdefdefgefghfghighijhijkijkljklmklmnlmnomnopnopq",
        NULL
      },
      .expected = {
        0x24, 0x8D, 0x6A, 0x61,
        0xD2, 0x06, 0x38, 0xB8,
        0xE5, 0xC0, 0x26, 0x93,
        0x0C, 0x3E, 0x60, 0x39,
        0xA3, 0x3C, 0xE4, 0x59,
        0x64, 0xFF, 0x21, 0x67,
        0xF6, 0xEC, 0xED, 0xD4,
        0x19, 0xDB, 0x06, 0xC1
      }
    },
    {
      .inputs = {
        "abcdbcdec",
        "defdefgefg",
        "hfghighijhi",
        "jkijkljklmkl",
        "mnlmnomnopnopq",
        NULL
      },
      .expected = {
        0x24, 0x8D, 0x6A, 0x61,
        0xD2, 0x06, 0x38, 0xB8,
        0xE5, 0xC0, 0x26, 0x93,
        0x0C, 0x3E, 0x60, 0x39,
        0xA3, 0x3C, 0xE4, 0x59,
        0x64, 0xFF, 0x21, 0x67,
        0xF6, 0xEC, 0xED, 0xD4,
        0x19, 0xDB, 0x06, 0xC1
      }
    },
    {
      .inputs = {
        "abcdbcdecdefdefgefghfghighijhijkijkljklmklmnlmnomnopnopq",
        "abcdbcdecdefdefgefghfghighijhijkijkljklmklmnlmnomnopnopq",
        NULL
      },
      .expected = {
        0x59, 0xf1, 0x09, 0xd9,
        0x53, 0x3b, 0x2b, 0x70,
        0xe7, 0xc3, 0xb8, 0x14,
        0xa2, 0xbd, 0x21, 0x8f,
        0x78, 0xea, 0x5d, 0x37,
        0x14, 0x45, 0x5b, 0xc6,
        0x79, 0x87, 0xcf, 0x0d,
        0x66, 0x43, 0x99, 0xcf
      }
    },
    {
      .inputs = {
        "abcdbcdecdefdefgefghfghighijhijkijkljklmklmnlmnomnopnopq"
            "abcdbcdecdefdefgefghfghighijhijkijkljklmklmnlmnomnopnopq",
        NULL
      },
      .expected = {
        0x59, 0xf1, 0x09, 0xd9,
        0x53, 0x3b, 0x2b, 0x70,
        0xe7, 0xc3, 0xb8, 0x14,
        0xa2, 0xbd, 0x21, 0x8f,
        0x78, 0xea, 0x5d, 0x37,
        0x14, 0x45, 0x5b, 0xc6,
        0x79, 0x87, 0xcf, 0x0d,
        0x66, 0x43, 0x99, 0xcf
      }
    },
    {
      .inputs = {
        "abcdbcdec",
        "defdefgefghfghi",
        "ghijhijkijkljklmklmnl",
        "mnomnopnopq",
        "abcdbcdecdefd",
        "efgefghfghighijhijkijkljklmklmnlmnomnopnopq",
        NULL
      },
      .expected = {
        0x59, 0xf1, 0x09, 0xd9,
        0x53, 0x3b, 0x2b, 0x70,
        0xe7, 0xc3, 0xb8, 0x14,
        0xa2, 0xbd, 0x21, 0x8f,
        0x78, 0xea, 0x5d, 0x37,
        0x14, 0x45, 0x5b, 0xc6,
        0x79, 0x87, 0xcf, 0x0d,
        0x66, 0x43, 0x99, 0xcf
      }
    },
  };

  for (i = 0; i < ARRAY_SIZE(tests); i++) {
    scrypt_sha256_test_t* v;
    uint8_t out[sizeof(v->expected)];
    size_t j;
    scrypt_sha256_t sha256;
    const char** chunk;

    v = &tests[i];

    scrypt_sha256_init(&sha256);
    for (chunk = v->inputs; *chunk != NULL; chunk++)
      scrypt_sha256_update(&sha256, (uint8_t*) *chunk, strlen(*chunk));
    scrypt_sha256_digest(&sha256, out);

    for (j = 0; j < ARRAY_SIZE(out); j++) {
      if (out[j] == v->expected[j])
        continue;

      fprintf(stderr,
              "0x%02x != 0x%02x at %d (test vector: %d)\n",
              out[j],
              v->expected[j],
              (int) j,
              (int) i);
      ASSERT(0, "sha256 failure");
    }
  }
}
